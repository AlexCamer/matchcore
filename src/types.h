#ifndef __PRIMATIVES_H__
#define __PRIMATIVES_H__

#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>
#include <sys/types.h>

/* floating point */
typedef float  f32;
typedef double f64;

/* signed integer */
typedef int8_t  i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;
typedef ssize_t isize;

/* unsigned integer */
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;
typedef size_t   usize;

#endif /* __PRIMATIVES_H__ */
